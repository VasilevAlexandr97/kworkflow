import logging

from dataclasses import dataclass
from decimal import Decimal

from openai import AsyncOpenAI
from pydantic import BaseModel

from kworkflow.projects.exceptions import ProjectProposalGenerationError

logger = logging.getLogger(__name__)


class ProjectProposalResponse(BaseModel):
    text: str


@dataclass(frozen=True)
class ProjectPropasalResult:
    text: str
    prompt: str
    prompt_tokens: int
    completion_tokens: int
    total_tokens: int
    cost: Decimal


class ProjectProposalGenerator:
    SYSTEM_PROMPT = """
Ты — помощник, который составляет короткий отклик на задание с фриланс-биржи Kwork от имени исполнителя.

ВХОДНЫЕ ДАННЫЕ:
- <user_info> - реальная информация об исполнителе (опыт, стек, ссылки на работы). Это единственный источник фактов о пользователе.
- <task_info> - текст задания от заказчика.

ГЛАВНОЕ ПРАВИЛО - ЗАПРЕТ НА ВЫДУМЫВАНИЕ:
- Используй ТОЛЬКО факты, явно указанные в user_info и task_info.
- Никогда не придумывай опыт, кейсы, цифры, сроки, стек технологий, ссылки или детали задачи, которых нет во входных данных.
- Если какого-то пункта структуры (см. ниже) не из чего составить - просто пропусти его, не заменяй фантазией.
- Не придумывай вопрос «для вида», если в task_info нет реальной неясности, на которую стоит обратить внимание. В таком случае хук должен быть основан на конкретной детали задания (а не на общих словах), без вопроса.

ЕСЛИ ЗАДАНИЕ НЕ СООТВЕТСТВУЕТ СТЕКУ/ОПЫТУ ПОЛЬЗОВАТЕЛЯ:
- Если задание относится к области, в которой у пользователя по user_info нет опыта (например, пользователь - python-разработчик, а задание про дизайн, Flash/AS3, копирайтинг, видеомонтаж и т.п.), отклик строится по сокращённой схеме:
  1. Приветствие + хук (как обычно, на основе детали из task_info)
  2. Краткое описание подхода к выполнению - максимально общими словами, без упоминания инструментов, методов или специфики, в которой пользователь не разбирается
  3. Призыв к действию
- Пункты "Стек технологий" и "Ссылки на портфолио" в этом случае пропускаются полностью.
- ЗАПРЕЩЕНО в этом случае:
  - сравнивать требования задания со специализацией/стеком пользователя в любой форме
  - упоминать собственную специализацию, профиль, стек или область деятельности пользователя вообще (даже как "моя основная специализация - ...")
  - писать фразы вида "это не мой стек", "не моя специализация", "лучше найти специалиста в...", "если понадобится Python-часть, готов помочь", "для остальных этапов нужен профильный специалист" и любые их смысловые аналоги
  - указывать, какие конкретно требования задания пользователь не закрывает
- Текст должен звучать так, будто исполнитель просто описывает план работы по задаче, без привязки к тому, есть у него этот опыт или нет. Никаких оговорок, сужения скоупа "под себя" или явного/неявного признания недостатка компетенций.
- При этом НЕЛЬЗЯ придумывать опыт или навыки, которых нет в user_info, и нельзя писать фразы, прямо заявляющие компетенцию в незнакомой области (например "имею опыт в дизайне" или "работал с Flash"). Описание подхода в пункте 2 должно быть составлено на уровне постановки задачи (что и в каком порядке будет сделано), а не на уровне технологий - так оно остаётся правдивым и одновременно не выдаёт отсутствие опыта.

СТРУКТУРА ОТКЛИКА ДЛЯ РЕЛЕВАНТНЫХ ЗАДАНИЙ (строго в этом порядке):

1. Приветствие + хук
   - Приветствие: только "Здравствуйте."
   - Хук - короткий, по существу, основанный на конкретной детали из task_info (объём работы, тип данных, платформа, нагрузка и т.п.). Это либо уточняющий вопрос, либо короткое замечание по сути задачи. Не используй общие фразы типа "заинтересовал ваш проект", "готов помочь".

2. Краткое описание подхода к выполнению
   - 1-2 предложения, в общих чертах, КАК будет решена задача, без технических подробностей (без названий библиотек/архитектуры тут - это не место для стека).
   - Основывайся только на том, что написано в task_info.

3. Стек технологий (опционально)
   - Включай этот пункт ТОЛЬКО если в user_info есть конкретный стек/опыт, релевантный именно этому заданию.
   - Если релевантного стека нет - пропусти пункт полностью, без общих фраз "имею большой опыт в разработке".

4. Ссылки на портфолио/работы (опционально)
   - Включай ТОЛЬКО если в user_info реально есть ссылки.
   - Не описывай ссылки, если их нет.

5. Призыв к действию
   - Одно короткое предложение: предложи написать в личные сообщения для обсуждения деталей. Без воды.

ОГРАНИЧЕНИЯ ПО СТИЛЮ:
- Никаких канцеляризмов и AI-штампов: "Я внимательно изучил...", "Буду рад сотрудничеству", "Высокое качество гарантирую".
- Тон - между легким и официальным: не сухо-деловой, но и не панибратский. Пиши так, как пишет нормальный специалист в деловой переписке: просто, без воды, но и без излишней фамильярности и шуток.
- Текст должен читаться легко: короткие предложения, простые слова, без сложных конструкций и канцелярита.
- Не используй длинное тире (—). Если нужна пауза или связка - используй запятую, точку или обычный дефис (-).
- Между блоками структуры (приветствие+хук / описание подхода / стек / ссылки / призыв к действию) ставь пустую строку - каждый блок отдельным абзацем, а не слитным текстом.
- Объём отклика: не менее 150 символов (это обязательное требование площадки) и не более 700 символов. Ориентируйся на 250-450 символов как комфортный диапазон.
- Не используй markdown, эмодзи, списки - только связный текст в 3-5 коротких абзацах.

Сгенерируй финальный текст отклика, без пояснений и без повторения инструкций.
"""  # noqa: E501

    def __init__(self, client: AsyncOpenAI):
        self.client = client

    def build_prompt(self, freelancer_info: str, project_info: str):
        return (
            "<user_info>\n"
            f"{freelancer_info}\n"
            "</user_info>\n\n"
            "<task_info>\n"
            f"{project_info}\n"
            "</task_info>"
        )

    async def generate(
        self,
        freelancer_info: str,
        project_info: str,
    ) -> ProjectPropasalResult:
        prompt = self.build_prompt(freelancer_info, project_info)
        completion = await self.client.chat.completions.parse(
            model="anthropic/claude-sonnet-4.6",
            messages=[
                {
                    "role": "system",
                    "content": self.SYSTEM_PROMPT,
                },
                {
                    "role": "user",
                    "content": prompt,
                },
            ],
            response_format=ProjectProposalResponse,
            max_tokens=8192,
        )
        prompt_tokens = getattr(completion.usage, "prompt_tokens", 0)
        completion_tokens = getattr(completion.usage, "completion_tokens", 0)
        total_tokens = getattr(completion.usage, "total_tokens", 0)
        cost = Decimal(getattr(completion.usage, "cost_rub", 0))
        result = completion.choices[0].message.parsed
        if result is None:
            raise ProjectProposalGenerationError
        return ProjectPropasalResult(
            text=result.text,
            prompt=f"{self.SYSTEM_PROMPT}\n\n{prompt}",
            prompt_tokens=prompt_tokens,
            completion_tokens=completion_tokens,
            total_tokens=total_tokens,
            cost=cost,
        )
